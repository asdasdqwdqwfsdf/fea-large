function K = integrate5nodes(el,F,varargin)
% 5-nodes cubic integration
    w1 = -4/5.;
    w2 = 9/20.;

    K = (w1*F(el,1/4.,1/4.,1/4.,varargin{:})+ ...
         w2*F(el,1/2.,1/6.,1/6.,varargin{:})+ ...
         w2*F(el,1/6.,1/2.,1/6.,varargin{:})+ ...
         w2*F(el,1/6.,1/6.,1/2.,varargin{:})+ ...
         w2*F(el,1/6.,1/6.,1/6.,varargin{:}))/6.;
end
